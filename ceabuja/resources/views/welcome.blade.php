    function sendComment(){
        var nameBox = document.getElementById("nameBox").value;
        var commentBox = $("#commentBox").val();

        if(nameBox != "" && commentBox != "") {


            var formData = {
                'name': nameBox,
                'comment': commentBox,
            };

            //alert("I got here");

            $.ajax({
                type: 'POST', // define the type of HTTP verb we want to use (POST for our form)
                url: '/addPrayerRequest', // the url where we want to POST
                data: formData,
                // dataType    : 'json', // what type of data do we expect back from the server
                encode: true
            })

            //  NProgress.start()
            // using the done promise callback
                .done(function (data) {

                    // alert("I dididid")
                    // log data to the console so we can see
                    // console.log("I got here too just now");
//
//                    //if number is on kingschat then process
                    if (data.status) {

                        NProgress.done();
                        $("#pray_info").notify(data.message, {
                            className: 'success', autoHide: true,
                        });

                        $("#commentBox").val("")
                        $("#nameBox").val("")



                    } else {
                        NProgress.done();
                        $.notify("Am error occured while trying to process your request, please try again later", {
                            className: 'error', autoHide: true,
                        });

                    }

                    // here we will handle errors and validation messages
                });

        }else{
            $("#pray_info").notify("All fields are required", {className: 'error',autoHide: true,
            } )
        }

    }




